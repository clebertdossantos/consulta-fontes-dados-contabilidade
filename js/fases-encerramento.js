exports.tituloFase = (name) => {
    name =  name
            .replace('- 2020','')
            .replace('- 2021','')
            .replace(' - SC','')
            .replace(' - MG','')
            .replace(' - BA','')
            .replace(' - RS','')
            .replace(' - RJ','')
            .replace(' - MT','')
            .replace(' - ES','')
            .replace(' - PR','')
            .replace('1 - ','')
            .replace('2 - ','')
            .replace('3 - ','')
            .replace('4 - ','')
            .replace('5 - ','')
            .replace('6 - ','')
            .replace('7 - ','')
            .replace('8 - ','')
            .replace('9 - ','')
            .replace('10 - ','')
    let arrayName = name.split(' ')
    if(arrayName[arrayName.length -1] === '' ){
        arrayName.splice(arrayName.length-1,1)
        name = arrayName.join(' ');
    }
    return name
}


